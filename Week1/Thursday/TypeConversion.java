public class TypeConversion {
    public static void main(String args[]){
        //widening
        //int to float
        int a = 10;
        System.out.println(a+1.2);

        //Narrowing
        float f = 10.99f;
        System.out.println((int) f); //this is like a floor function

    }
    
}
