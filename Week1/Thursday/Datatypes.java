// package Week1.Thursday;

public class Datatypes {
    public static void main(String[] args){

        //1. byte integer
        byte byteInteger = 10;
        System.out.println("Byte Integer: "+ byteInteger);

        //2. short integer
        short shortInteger = 10;
        System.out.println("Byte Integer: "+ shortInteger);
        
        //3. integer
        int integer = 10;
        System.out.println("Integer: "+ integer);
        
        //4. long integer
        long longInteger = 100L; //this L is unneccesary, java knows it is a long becaue of "long"
        System.out.println("Long Integer: "+ longInteger);
        
        //5. Float
        float floatNumber = 10.123f; //the f in the end is important
        System.out.println("Float: "+ floatNumber);

        //6. Double
        float doubleNumber = 2.23e20f; 
        System.out.println("Double: "+ doubleNumber); 
        
        //7. Bool
        boolean bool = true; 
        System.out.println("Boolean: "+ bool);    
        
        //8. Char
        char character = 'a'; 
        System.out.println("Char: "+ character);            
        
    }
    
}
